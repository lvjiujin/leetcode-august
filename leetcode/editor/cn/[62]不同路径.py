class Solution:

    def uniquePaths(self, m: int, n: int) -> int:
        # ç©ºé—´ä¼˜åŒ–
        dp = [1 for _ in range(n)]
        for i in range(1, m):
            for j in range(1, n):
                dp[j] += dp[j-1]

        return dp[-1]

    # æ’åˆ—ç»„åˆ,å› ä¸ºæœºå™¨ä»å·¦ä¸Šè§’åˆ°å³ä¸‹è§’ï¼Œæ€»å…±å‘å³ï¼Œå‘ä¸‹èµ°çš„æ­¥æ•°æ˜¯ç¡®å®šçš„ã€‚mè¡Œï¼Œnåˆ—ï¼Œå³å‘ä¸‹èµ°m-1,å‘å³èµ°n-1ï¼Œå³çœ‹æ˜¯å…ˆä¸‹è¿˜æ˜¯å…ˆå³è€Œå·²ã€‚
    def uniquePaths2(self, m: int, n: int) -> int:
        # math.factorial(x)æ˜¯xçš„é˜¶ä¹˜çš„æ„æ€
        return int(math.factorial(m + n - 2) / math.factorial(m - 1) / math.factorial(n - 1))

    # åŠ¨æ€è§„åˆ’ äºŒç»´æ•°ç»„ï¼Œä¸€è¡Œä»£ç å®ç°äºŒç»´æ•°ç»„å®šä¹‰ï¼Œé¦–è¡Œä¸º1ï¼Œé¦–åˆ—ä¸º1.
    def uniquePaths1(self, m: int, n: int) -> int:
        dp = [[1] * n] + [[1] + [0] * (n-1) for _ in range(m-1)]
        for i in range(1, m):
            for j in range(1, n):
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1]
        return dp[-1][-1]

