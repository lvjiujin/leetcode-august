class Solution:
    def maxDepth(self, root: Optional[TreeNode]) -> int:
        # æ–¹æ³•ä¸€ï¼šé€’å½’æ³•:
        # if not root:
        #     return 0
        #
        # return max(self.maxDepth(root.left), self.maxDepth(root.right)) + 1
        # æ–¹æ³•äºŒï¼šå¹¿åº¦ä¼˜å…ˆ:
        if not root:
            return 0
        max_dep = 0
        queue = [root]
        while queue:
            max_dep += 1
            for _ in range(len(queue)):
                node = queue.pop(0)
                if node.left:
                    queue.append(node.left)
                if node.right:
                    queue.append(node.right)
        return max_dep



