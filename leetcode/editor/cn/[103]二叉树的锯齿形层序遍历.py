class Solution:
    def zigzagLevelOrder(self, root: Optional[TreeNode]) -> List[List[int]]:
        # å±‚åºéå†ï¼Œäº¤æ›¿äº¤æ¢æ¯ä¸€å±‚çš„é¡ºåºã€‚
        if not root:
            return []
        output, queue = [], [root]
        flag = False
        while queue:
            size = len(queue)
            flag = False if flag else True
            vals = []
            for _ in range(size):
                r = queue.pop(0)
                vals.append(r.val)
                if r.left:
                    queue.append(r.left)
                if r.right:
                    queue.append(r.right)
            if not flag:
                vals = vals[::-1]
            output.append(vals)
        return output


